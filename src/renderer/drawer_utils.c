#include "veclib.h"
#include "mlx.h"

mlx_color	vec3_to_color(t_vec3 v)
{
	return ((mlx_color){
		.r = (int)v.x,
		.g = (int)v.y,
		.b = (int)v.z,
		.a = 255
	});
}
